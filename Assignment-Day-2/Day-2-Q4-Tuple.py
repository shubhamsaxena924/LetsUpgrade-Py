tup = (1, 2, 3, 2, 2)
print('Tuple:', tup)

print('Length of my tuple:', len(tup))  # Length of the tuple

print('Count of 2:', tup.count(2))  # count of 2

print('Index of 3:', tup.index(3))  # index of 3

print('Max:', max(tup))  # maximum value of tup

print('Min:', min(tup))  # min value of tuple

print('Tuple2:', tuple(['Hello', 'Bye', 12]))  # converts a list into tuple
